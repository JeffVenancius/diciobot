function getword() {
	axios.get('https://www.dicio.com.br' + links[Math.floor(Math.random() * links.length)])
		.then(function (response) {
			// handle success
			const data = parse(response.data)
			const title = data.querySelector('.title-header h1').text.toLowerCase()
			const texts = data.querySelectorAll('.significado span')
			let full = title.match(/[a-z].*[a-z]/)[0].replace(/\b\w/g, s => s.toUpperCase());

			texts.forEach(i => {
				let textArr = i.text.split('')
				textArr[0] = textArr[0].toUpperCase()
				full += '\n' + textArr.join('')
			})

			while (full.length > 300) {
				let fullArr = full.split('\n')
				fullArr.pop()
				full = fullArr.join('\n')
			}
			return full

		})
		.catch(function (error) {
			// handle error
			return ''
		})
		.finally(function () {
			return ''
		});
}


